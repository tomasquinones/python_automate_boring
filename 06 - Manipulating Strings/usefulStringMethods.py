# Useful String Methods

test_string = 'the quick brOwn fOx jUmps oVer tHe lazy dogs'

print('This upper() method changes all letters to upper case:')
print(test_string.upper())

print('\n')


print('This lower() method changes all letter to lower case')
print(test_string.lower())

print('\n')


print('The isalpha() returns true if the string is all letters')
print(test_string.isalpha())

print('\n')

